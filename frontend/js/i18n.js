export function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang] && i18n[lang][key]) {
            if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'number')) {
                el.placeholder = i18n[lang][key];
            } else {
                // If there are child nodes like icons, we need to preserve them.
                // We assume the translated text replaces the last Text Node or we wrap text in a span.
                // Actually it's safer if the developer applies data-i18n only to elements that just contain text.
                // But for now, we just set textContent if there are no element children, or just innerHTML.
                // It is better to use innerHTML if we have nested spans.
                el.innerHTML = el.innerHTML.replace(el.textContent.trim(), i18n[lang][key]);
            }
        }
    });
}
